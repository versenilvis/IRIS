func lookupIcon(key string) string {
	key = strings.ToLower(strings.TrimSpace(key))
	if icon, ok := iconMap[key]; ok {
		return icon
	}
	if len(key) > 0 && key[0] >= '0' && key[0] <= '9' {
		return ""
	}
	return "❯"
}
